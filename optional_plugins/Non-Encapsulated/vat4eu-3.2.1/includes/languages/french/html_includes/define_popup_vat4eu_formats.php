        <li><b>9</b>: Un chiffre</li>
        <li><b>X</b>: Une lettre ou un chiffre</li>
        <li><b>S</b>: Une lettre, un chiffre, <b>+</b> ou <b>*</b></li>
        <li><b>L</b>: Une lettre</li>
    </ul>

