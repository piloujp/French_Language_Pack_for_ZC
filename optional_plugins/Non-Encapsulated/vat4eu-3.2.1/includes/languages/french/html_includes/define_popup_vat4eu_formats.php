    <p><strong>Remarques :</strong></p>
    <ul id="vat4eu-notes">
        <li><sup>1</sup> La 1ère position suivant le préfixe est toujours <b>U</b>.</li>
        <li><sup>2</sup> Le premier chiffre suivant le préfixe est toujours zéro (<b>0</b>).</li>
        <li><sup>3</sup> Le (nouveau) format à 10 chiffres est le résultat de l'ajout d'un zéro non significatif au (ancien) format à 9 chiffres.</li>
        <li><sup>4</sup> Le premier et le dernier caractère peuvent être alphabétiques ou numériques, mais ils ne peuvent pas être tous les deux numériques.</li>
        <li><sup>5</sup> Identifie les commerçants de succursales.</li>
        <li><sup>6</sup> Identifie les ministères.</li>
        <li><sup>7</sup> Identifie les autorités sanitaires.</li>
        <li><sup>8</sup> La 10ème position suivant le préfixe est toujours <b>B</b>.</li>
    </ul>
