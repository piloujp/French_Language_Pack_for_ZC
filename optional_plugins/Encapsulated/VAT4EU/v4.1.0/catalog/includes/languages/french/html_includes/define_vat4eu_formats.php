<p class="py-2"><strong>Remarques :</strong></p>
<ul id="vat4eu-remarks" class="list-unstyled">
    <li>*: Le format exclut les 2 lettres du préfixe</li>
    <li>9: Un chiffre</li>
    <li>X: Une lettre ou un chiffre</li>
    <li>S: Une lettre; un chiffre; '+' ou '*'</li>
    <li>L: Une lettre</li>
</ul>
<p class="py-2"><strong>Notes :</strong></p>
<ul id="vat4eu-notes" class="list-unstyled">
    <li><sup>1</sup> La 1ère position suivant le préfixe est toujours <b>U</b>.</li>
    <li><sup>4</sup> Le premier et le dernier caractère peut être une lettre ou un chiffre; Mais ils ne peuvent pas être tous les deux des chiffres.</li>
    <li><sup>5</sup> Identifie les succursale.</li>
    <li><sup>6</sup> Identifie les ministères.</li>
    <li><sup>7</sup> Identifie les autorités sanitaires.</li>
    <li><sup>8</sup> Veuillez respecter la casse. Veuillez suivre scrupuleusement la syntaxe du numéro de TVA indiqué.</li>
</ul>
