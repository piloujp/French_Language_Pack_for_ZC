<p class="py-2"><strong>Remarques :</strong></p>
<ul id="vat4eu-notes" class="list-unstyled">
    <li><sup>1</sup> La 1ère position suivant le préfixe est toujours <b>U</b>.</li>
    <li><sup>2</sup> Si le client fournit un numéro de TVA à 9 chiffres, <b>0</b> doit être ajouté comme premier chiffre.</li>
    <li><sup>3</sup> Les lettres peuvent être incluses comme premier et/ou deuxième caractère (sauf les lettres O et I).</li>
    <li><sup>4</sup> Les lettres peuvent être incluses comme dernier, ou deuxième et dernier, ou 2 derniers caractères.</li>
    <li><sup>5</sup> La 10ème position suivant le préfixe est toujours <b>B</b>.</li>
    <li><sup>6</sup> Le premier et/ou le dernier caractère peut être une lettre.</li>
</ul>
